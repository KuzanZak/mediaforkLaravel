    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroyService($id)
    {
        $service = Service::find($id);
        $service->delete();
        return Redirect::route('dashboard/service');
